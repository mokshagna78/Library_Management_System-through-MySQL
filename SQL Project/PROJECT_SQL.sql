-- How many copies of the book titled "The Lost Tribe" are owned by the library branch whose name is "Sharpstown"
SELECT SUM(NUM_OF_COPIES) FROM BOOKS, COPIES, BRANCHES
WHERE BOOKS.B_ID = COPIES.COPY_BOOK_ID
AND COPIES.BRANCHID = BRANCHES.BRANCH_ID
AND BOOKS.B_TITLE = 'The Lost Tribe'
AND BRANCHES.BRANCH_NAME = 'Sharpstown';

-- How many copies of the book titled "The Lost Tribe" are owned by each library branch
SELECT BRANCHES.BRANCH_NAME,SUM(NUM_OF_COPIES) FROM BOOKS, COPIES, BRANCHES
WHERE BOOKS.B_ID = COPIES.COPY_BOOK_ID
AND COPIES.BRANCHID = BRANCHES.BRANCH_ID
AND BOOKS.B_TITLE = 'The Lost Tribe'
GROUP BY BRANCHES.BRANCH_NAME;

-- Retrieve the names of all borrowers who do not have any books checked out
SELECT B_NAME FROM BORROWER
LEFT JOIN LOANS
ON BORROWER.CARD_NUM = LOANS.L_CARDNO
WHERE LOANS.L_CARDNO IS NULL;

-- For each book that is loaned out from the "Sharpstown" branch and whose DueDate is 2/3/18, 
-- retrieve the book title, the borrower's name, and the borrower's address. 
SELECT BOOKS.B_TITLE,BORROWER.B_NAME,BORROWER.B_ADDRESS FROM LOANS
JOIN BOOKS ON LOANS.L_BOOKID = BOOKS.B_ID
JOIN BORROWER ON LOANS.L_CARDNO = BORROWER.CARD_NUM
JOIN BRANCHES ON LOANS.L_BRANCHID = BRANCHES.BRANCH_ID
WHERE BRANCHES.BRANCH_NAME = 'Sharpstown'
AND LOANS.L_DUEDATE = '2/3/18';

-- For each library branch, retrieve the branch name and the total number of books loaned out from that branch
SELECT BRANCHES.BRANCH_NAME,COUNT(*) AS TOTAL_NO_OF_BOOKS FROM LOANS
JOIN BRANCHES ON LOANS.L_BRANCHID = BRANCHES.BRANCH_ID
GROUP BY BRANCHES.BRANCH_NAME;

-- Retrieve the names, addresses, and number of books checked out for all borrowers who have more than five books checked out
SELECT BORROWER.B_NAME,BORROWER.B_ADDRESS,COUNT(*) FROM BORROWER
JOIN LOANS ON BORROWER.CARD_NUM = LOANS.L_CARDNO
GROUP BY BORROWER.CARD_NUM
HAVING COUNT(*) > 5;

-- For each book authored by "Stephen King", retrieve the title and the number of copies owned by the library branch whose name is "Central"
SELECT BOOKS.B_TITLE,COPIES.NUM_OF_COPIES FROM BOOKS
JOIN AUTHORS ON BOOKS.B_ID = AUTHORS.BOOK_ID
JOIN COPIES ON BOOKS.B_ID = COPIES.COPY_BOOK_ID
JOIN BRANCHES ON COPIES.BRANCHID = BRANCHES.BRANCH_ID
WHERE AUTHORS.AUTHOR = 'Stephen King'
AND BRANCHES.BRANCH_NAME = 'Central';
