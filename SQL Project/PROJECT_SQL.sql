CREATE DATABASE LIBRARY_DB;
USE LIBRARY_DB;

-- PUBLISHER
CREATE TABLE PUBLISHER(
P_NAME VARCHAR(255) PRIMARY KEY,
P_ADDRESS TEXT,P_PHONE VARCHAR(100));

-- BOOKS
CREATE TABLE BOOKS(
B_ID INT PRIMARY KEY,
B_TITLE VARCHAR(200),B_PUBLISHER_NAME VARCHAR(255),
FOREIGN KEY(B_PUBLISHER_NAME) REFERENCES PUBLISHER(P_NAME)
);

-- AUTHORS
CREATE TABLE AUTHORS(
B_AUTHOR_ID INT PRIMARY KEY AUTO_INCREMENT,
BOOK_ID INT,AUTHOR VARCHAR(200),
FOREIGN KEY(BOOK_ID) REFERENCES BOOKS(B_ID)
);

-- LIBRARY BRANCHES
CREATE TABLE BRANCHES(
BRANCH_ID INT PRIMARY KEY AUTO_INCREMENT,
BRANCH_NAME VARCHAR(200),BRANCH_ADDRESS TEXT
);

-- BOOKS COPIES
CREATE TABLE COPIES(
COPY_ID INT PRIMARY KEY AUTO_INCREMENT,
COPY_BOOK_ID INT,BRANCHID INT,NUM_OF_COPIES INT,
FOREIGN KEY(COPY_BOOK_ID) REFERENCES BOOKS(B_ID),
FOREIGN KEY(BRANCHID) REFERENCES BRANCHES(BRANCH_ID)
);

-- BORROWER
CREATE TABLE BORROWER(
CARD_NUM INT PRIMARY KEY,B_NAME VARCHAR(200),
B_ADDRESS VARCHAR(200),B_PHONE VARCHAR(100)
);

-- LOANS
CREATE TABLE LOANS(
LOAN_ID INT PRIMARY KEY AUTO_INCREMENT,
L_BOOKID INT,L_BRANCHID INT,L_CARDNO INT,
L_DATEOUT DATE,L_DUEDATE DATE,
FOREIGN KEY(L_BOOKID) REFERENCES BOOKS(B_ID),
FOREIGN KEY(L_BRANCHID) REFERENCES BRANCHES(BRANCH_ID),
FOREIGN KEY(L_CARDNO) REFERENCES BORROWER(CARD_NUM)
);


